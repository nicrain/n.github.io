        <h3>Voitures</h3>
        <p>
            Les voitures de Formule 1 modernes sont des monoplaces à moteur central, hybrides, à cockpit semi-ouvert et
            à roues ouvertes.
            Le châssis est composé en grande partie de matériaux composites à base de fibres de carbone, ce qui le rend
            léger tout en étant extrêmement rigide et solide.
            L'ensemble de la voiture, pilote et carburant inclus, ne pèse que 795 kg, soit le poids minimum fixé par le
            règlement.
            Si la voiture est plus légère que le minimum requis, elle peut être lestée pour atteindre le poids
            nécessaire.
            Les équipes de course profitent de cette possibilité pour placer le lest à l'extrémité inférieure du
            châssis, ce qui permet d'abaisser le centre de gravité et d'améliorer ainsi la maniabilité et le transfert
            de poids.
        </p>
