        <h3>Technologies</h3>
        <h4>Vitesse en virage</h4>
        <p>
            La vitesse de passage en courbe des voitures de Formule 1 est largement déterminée par la force
            aérodynamique qu'elles génèrent et qui pousse la voiture vers le bas de la piste.
            Cette force est fournie par les « ailes » situées à l'avant et à l'arrière du véhicule, ainsi que par
            l'effet de sol créé par la faible pression de l'air au niveau du fond plat de la voiture.
            La conception aérodynamique des voitures est très contraignante pour limiter les performances.
            La génération précédente de voitures était équipée d'un grand nombre de petites ailettes, de « barge boards
            » et d'ailettes de virage conçues pour contrôler étroitement le flux d'air au-dessus, au-dessous et autour
            de la voiture.
            <br><br>
            L'autre facteur important qui influence la vitesse de passage en courbe des voitures est la conception des
            pneus.
            De 1998 à 2008, les pneus de la Formule 1 n'étaient pas des « slicks » (pneus sans sculptures), comme dans
            la plupart des autres séries de courses sur circuit.
            Au lieu de cela, chaque pneu présentait quatre grandes rainures circonférentielles sur sa surface, conçues
            pour limiter la vitesse des voitures dans les virages.
            Les pneus slicks sont revenus en Formule 1 lors de la saison 2009.
            La suspension est à double triangulation ou multibras à l'avant et à l'arrière, avec des ressorts et des
            amortisseurs actionnés par des bielles sur le châssis,
            à l'exception de la voiture Red Bull Racing (RB5) qui utilise une suspension à biellettes à l'arrière, ce
            qui fait de cette voiture la première depuis la Minardi PS01 en 2001.
            Ferrari a utilisé une suspension à biellettes à l'avant et à l'arrière dans sa voiture de 2012.
            Ferrari (F138) et McLaren (MP4-28) de la saison 2013 ont utilisé une suspension à biellettes à l'avant et à
            l'arrière. En 2022, McLaren (MCL36) et Red Bull Racing (RB18) ont adopté une suspension avant et une
            suspension arrière à biellettes.
        </p>

        <h4>Freins</h4>
        <p>
            Les freins à disque en carbone-carbone sont utilisés pour réduire le poids et améliorer les performances de
            friction.
            Ils offrent un très haut niveau de performance de freinage et sont généralement l'élément qui provoque la
            plus grande réaction des pilotes qui découvrent cette technologie.
            Le matériau carbone permet d'améliorer les performances des freins, même dans des conditions de chaleur
            extrême. Pour optimiser cet aspect, les freins sont dotés de 1 000 trous d'aération, ce qui garantit des
            performances et un refroidissement optimaux.
        </p>
        <h4>Aérodynamique</h4>
        <p>
            En 2022, le règlement technique a été considérablement modifié afin de réduire les turbulences (communément
            appelées « air sale ») produites par l'aérodynamique de la voiture.
            Ces modifications concernent notamment l'aileron avant et arrière redessiné, les roues plus grandes avec un
            profil de pneu plus bas, les enjoliveurs,
            les petits winglets, l'interdiction des planches de barge et la réintroduction de la production de force
            descendante par effet de sol.
            Ces modifications visent à promouvoir la course automobile, ce qui signifie que les voitures perdent moins
            de force d'appui lorsqu'elles en suivent une autre.
            Cela permet aux voitures d'en suivre une autre à une distance beaucoup plus courte, sans augmenter l'écart
            en raison des turbulences (voir les règlements techniques 2022 du championnat du monde de Formule 1).
        </p>
        <h4>Roues</h4>
        <p>
            Les voitures de Formule 1 doivent avoir quatre roues fabriquées dans l'un des deux alliages de magnésium
            spécifiés par la Fédération internationale de l'automobile (FIA).
            Les roues en alliage de magnésium fabriquées par forgeage permettent de réduire au maximum le poids rotatif
            non suspendu.
            À partir de 2022, les roues sont recouvertes d'enjoliveurs « spec » standardisés, le diamètre des roues est
            passé de 13 à 18 pouces (réduisant ainsi le « profil du pneu »), et de petites ailettes ont été placées
            au-dessus des pneus avant.
        </p>
        <h4>Moteurs</h4>
        <p>
            Depuis la saison 2014 de Formule 1, les moteurs sont passés d'un V8 atmosphérique de 2,4 litres à des V6
            turbocompressés de 1,6 litre,
            qui tirent une grande partie de leur puissance de moteurs électriques.
            Ils intègrent également de nombreuses technologies de récupération d'énergie.
            Les moteurs fonctionnent avec du carburant sans plomb qui ressemble beaucoup à l'essence disponible dans le
            commerce.
            L'huile lubrifiante et protectrice du moteur a une viscosité très proche de celle de l'eau.
            Les moteurs de la génération 2006 tournaient à 20 000 tr/min et produisaient plus de 580 kW (780 ch).
            En 2007, les moteurs ont été limités à 19 000 tr/min en raison du gel des spécifications des moteurs depuis
            la fin 2006, et ont vu leurs zones de développement limitées.
            Pour la saison 2009 de Formule 1, les moteurs ont encore été limités à 18 000 tr/min.
        </p>

