        <h3>Directeurs d'équipe</h3>
        <p>
            Susie Wolff est l'actuelle directrice générale de la F1 Academy, mais elle est présente dans le paddock de
            la Formule 1 depuis plusieurs décennies.
            Au début de sa carrière, on lui a demandé de conduire une voiture rose.
            Bien qu'elle ait été sceptique au départ, elle a accepté en pensant que les jeunes filles verraient la
            voiture et se sentiraient ainsi plus inspirées pour se lancer dans la course.
            En 2012, elle a été pilote de développement pour Williams Grand Prix Engineering.
            Au cours de ses quatre années chez Williams, elle a participé à plusieurs séances d'essais libres pour
            l'équipe.
            Après son passage chez Williams, elle est devenue directrice de l'équipe de Formule E Venturi Racing, et a
            mené l'équipe à sa saison la plus réussie à ce jour.
