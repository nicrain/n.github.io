        <h3>Les pilotes féminines de Formule 1</h3>
        <p>
            Maria Teresa de Filippis est la première femme à avoir participé à un Grand Prix de Formule 1, lors du Grand
            Prix de Monaco 1958.
            Elle a participé à cinq Grands Prix au total, sous le drapeau italien, et a été saluée comme une pionnière
            du sport automobile féminin.
            Lella Lombardi est la seule femme à s'être classée dans les points lors d'un Grand Prix de Formule 1.
            Elle a participé à trois saisons, prenant part à dix-sept courses et démarrant douze fois.
            Après être arrivée sixième lors du Grand Prix d'Espagne 1975, Lombardi est devenue la première et la seule
            femme à marquer des points lors d'un Grand Prix officiel de Formule 1.
            La course n'ayant pas été menée à son terme, la moitié des points ont été attribués et Lombardi n'a gagné
            que 0,5 point.
            Desiré Wilson est la seule femme à avoir remporté une course de Formule 1, quelle qu'elle soit, en
            remportant la deuxième manche du championnat Aurora AFX F1 de 1980.
        </p>

        <h3>Ingénieurs de course</h3>
